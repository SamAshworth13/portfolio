package week1Ex4;

public class Player 
{
	private int hp = 40;
	private int score = 0;
	
	
	public int getHP()
	{
		return hp;
	}
	
	public void setHP(int newHP)
	{
		this.hp = newHP;
	}
	
	public int getScore()
	{
		return score;
	}
	
	public void setScore(int newScore)
	{
		this.score = newScore;
	}
	
}
